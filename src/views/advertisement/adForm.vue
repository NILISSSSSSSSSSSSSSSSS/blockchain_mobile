<script>
import { isNumber } from "assets/js/utils";
import { submitAdver  } from "api/advertisement";
import { getTranInfo } from "api/api";
import { getUserDetail } from 'api/advertisement'
export default {
    props: {
        currencyArr: Array,
        fiatCurrency: Array,
        exchangeArr: Array,
        overPercentLimit: Object,
        payTypeCanUseArr: Array
    },
    data() {
        return {
             authlist: {
                twoPhase:true,
                phone:false,
                email:false
             },
            selectCurrency: false,
            selectTradeType: false,
            selectFiatCurrency: false,
            selectExchange: false,
            tradeTypeArr: [this.$t("common.gouMai"), this.$t("common.chushou")],
            fiatCurrencyArr: [],
            exchangeValue: 0,
            forbiddenSubmit: true,
             authMode: "phone",
             countDown: 0,
             configFiatBi:"",
            form: {
                currency: "",
                fiatCurrency: "CNY",
                tradeType: "buy",
                allowPay: [],
                isUSDT: false,
                exchange: "average",
                overPercent: 0,
                limitPrice: null,
                orderFloorPrice: null,
                orderCeilPrice: null,
                lockMinute: 15,
                maxOrderLimit: 5,
                tradeCountLimit: 0,
                isIdentityVerify: false,
                isadvancedVerify: false,
                autoReplyBefore: "",
                phoneCode:"",
                emailCode:""
            }
        };
    },
    created(){
         this.configFiatBi= localStorage.getItem("configFiatBi");
        this._getUserDetail()
        },
    computed: {
         canSend() {
            return this.countDown > 0;
            },
        tradeTypeText() {
            return this.form.tradeType === "buy" ? this.$t("common.gouMai") : this.$t("common.chushou");
        },
        payArr() {
            for (let i of this.fiatCurrency) {
                if (i.name === this.form.fiatCurrency) {
                    let tmp = []
                    i.allowPay.forEach((payType) => {
                        let obj = {
                            payType: payType
                        }
                         this.payTypeCanUseArr.forEach(cc=>{
                            if(cc.payType == payType){
                                obj.canUse = cc.isOrderShow
                            }
                        })
                        tmp.push(obj)
                    })
                    console.log(tmp)
                    return tmp;
                }
            }
            return [];
        },
        exchanges() {
            return this.exchangeArr.map(x => x.name);
        },
        exchangeText() {
            for (let i of this.exchangeArr) {
                if (i.name === this.form.exchange) {
                    this.exchangeValue = i.value;
                    return `${i.name}(${i.value.toFixed(2)})`;
                }
            }
        },
        overPercentScope() {
            if (this.overPercentLimit.min) {
                return `${this.accuracy(
                    this.overPercentLimit.min
                )}% - ${this.accuracy(this.overPercentLimit.max)}%`;
            }
            return "";
        },
        overPercentPrice() {
            if (!isNumber(this.form.overPercent))
                return this.exchangeValue.toFixed(2);
            return (
                +this.exchangeValue *
                (1 + this.accuracy2(this.form.overPercent))
            ).toFixed(2);
        },
        textBest() {
            return this.form.tradeType === "buy"
                ? this.$t("common.jieShouZuiGao")
                : this.$t("common.jieShouZuiDiJia");
        },
        textFloor() {
            return this.form.tradeType === "buy"
                ? `${this.$t("common.zuiXiaoXianE")}(${this.form.currency})`
                : `${this.$t("common.zuiXiaoXianE")}(${this.form.fiatCurrency})`;
        },
        textCeil() {
            return this.form.tradeType === "buy"
                ? `${this.$t("common.zuiDaXianE")}(${this.form.currency})`
                : `${this.$t("common.zuiDaXianE")}(${this.form.fiatCurrency})`;
        },
        textTrade() {
            return this.form.tradeType === "buy" ? this.$t("common.mai1") : this.$t("common.mai2");
        },
        textUSDT() {
            return this.form.tradeType === "buy"
                ? this.$t("common.ask3",{msg:this.configFiatBi})
                : this.$t("common.ask2",{msg:this.configFiatBi});
        }
    },
  watch: {
    fiatCurrency: {
      handler(val) {
        this.fiatCurrencyArr = val.map(x => x.name)
      },
      deep: true
    },
    currencyArr:{
         handler(val) {
         this.form.currency=val[0]
      },
      deep: true
    },
    form: {
      handler(val) {
        this.canSubmit()
      },
      deep: true
    }
  },
    destroyed() {
    clearInterval(this.timmer);
    },
  methods: {
        _getUserDetail(){
      getUserDetail().then((res) => {
        if(res.code == 0){
            this.authlist.phone=res.data.phone
            this.authlist.email=res.data.email
            this.authlist.twoPhase=res.data.twoPhase

            if(this.authlist.phone){
                  this.authMode="phone"
                }
                else if(!this.authlist.phone && this.authlist.email){
                  this.authMode="email"
            }

            console.log(this.authlist)
        //    this.authlist = res.data.authlist || {};
        }
      })
    },
     
  
       // 获取验证码
    getCode() {
      this._countDown();
       let data={};
       console.log(this.authMode)
       this.authMode=="phone" ?  data.authMode="phone" :  data.authMode="email";
       const url = "/shop/ad_sendcode";
          this.$http
                .post(url, data)
                .then(res => {
                    if (res.data.code == 0) {
                        this.$toast.success(this.$t("yanZheng.yanZhengMaTips"));
                    } else {
                        this.$toast.fail(res.msg[0]);
                    }
                })
    },

       // 倒计时
    _countDown(ms = 1000 * 60) {
      const now = new Date().getTime();
      const end = now + ms;
      this.timmer = setInterval(() => {
        this.countDown = ((end - new Date().getTime()) / 1000).toFixed(0);
        if (this.countDown <= 0) clearInterval(this.timmer);
      }, 1000);
    },
         selectAuthMode(index) {
            if (index === 0) {
                this.authMode = "phone";
            } else if (index === 1) {
                this.authMode = "email";
            };
            
    },
      gotoSetting(payType){
          this.$router.push(`/account/tradeSetting/${payType}`)
      },
      // 发布广告
      _submitAdver() {
          if (!this._isSubmit()) return;
            let data = Object.assign({}, this.form, {
                  overPercent: this.accuracy2(this.form.overPercent)
              })

            if (!this.authlist.twoPhase && this.authMode=="phone") {
                data.authMode="phone";
                data.authCode=this.form.phoneCode;
            }
            if (!this.authlist.twoPhase && this.authMode=="email") {
                data.authMode="email";
                data.authCode=this.form.emailCode;
            }
            console.log(data)
          submitAdver(data
          ).then(data => {
              if (data.code !== 0) return;
              this.$toast("发布成功");
              const advertId = data.data.advertId;
              this.$router.push(`/advertisement/detail/${advertId}`);
          });
      },
      // 小数=》百分比
      accuracy(num) {
          return num * 100;
      },
      // 百分比=》小数
      accuracy2(num) {
          let str = String(num);
          if (str.indexOf(".") === -1) return num / 100;
          const cifang = str.split(".")[1].length;
          return num * Math.pow(10, cifang) / (100 * Math.pow(10, cifang));
      },
      // 能否点击发布交易
      canSubmit() {
          this.forbiddenSubmit = true;
          for (let x in this.form) {
              if (this.form.hasOwnProperty(x)) {
                  if (this.form[x] == null) return;
                  if (
                      this.form[x] instanceof Array &&
                      this.form[x].length === 0
                  )
                      return;
              }
          }
          this.forbiddenSubmit = false;
      },
      onChangeCurrency(currency) {
          this.selectCurrency = false;
          this.form.currency = currency;
          this.initForm();
          this.$emit("updatePreset", this.form);
      },
      onChangeTradeType(tradeType) {
          this.selectTradeType = false;
          this.form.tradeType = tradeType === "购买" ? "buy" : "sell";
          this.initForm();
          this.$emit("updatePreset", this.form);
      },
      onChangeFiatCurrency(fiatCurrency) {
          this.selectFiatCurrency = false;
          this.form.fiatCurrency = fiatCurrency;
          this.initForm();
          this.$emit("updatePreset", this.form);
      },
      onChangeExchange(exchange) {
          this.selectExchange = false;
          this.form.exchange = exchange;
      },
      onChangeOverPercent(num) {
          if (num === "-" || num === "") return;
          if (!isNumber(num)) {
              this.form.overPercent = 0;
          }
      },
      // 切换选项清除一些数据
      initForm() {
          this.form.exchange = "average";
          this.form.allowPay = [];
          this.form.orderFloorPrice = null;
          this.form.orderCeilPrice = null;
          this.form.limitPrice = null;
      },
      // 发布交易合法性校验
      _isSubmit() {
          if (
              !isNumber(this.form.orderFloorPrice) ||
              !isNumber(this.form.orderCeilPrice) ||
              !isNumber(this.form.limitPrice)
          ) {
              this.$toast.fail("限额必须为数字");
              return false;
          }
          if (
              Number(this.form.overPercent) <
                  this.overPercentLimit.min * 100 ||
              Number(this.form.overPercent) >
                  this.overPercentLimit.max * 100 ||
              isNaN(Number(this.form.overPercent))
          ) {
              this.$toast.fail("溢价设置超过限制");
              return false;
          }
          if (
              Number(this.form.orderFloorPrice) < 0 ||
              Number(this.form.orderCeilPrice) < 0
          ) {
              this.$toast.fail("限额必须大于0");
              return false;
          }
          //限额保留小数点后的位数的限制
          if (this.form.orderFloorPrice.toString().indexOf(".") > -1) {
              if (this.form.tradeType == "buy") {
                  if (
                      this.form.orderFloorPrice.replace(/^\d+\./, "").length >
                      8
                  ) {
                      this.$toast.fail("数字币最多保留小数点后8位");
                      return false;
                  }
              } else {
                  if (
                      this.form.orderFloorPrice.replace(/^\d+\./, "").length >
                      2
                  ) {
                      this.$toast.fail("法币最多保留小数点后2位");
                      return false;
                  }
              }
          }
          if (this.form.orderCeilPrice.toString().indexOf(".") > -1) {
              if (this.form.tradeType == "buy") {
                  if (
                      this.form.orderCeilPrice.replace(/^\d+\./, "").length >
                      8
                  ) {
                      this.$toast.fail("数字币最多保留小数点后8位");
                      return false;
                  }
              } else {
                  if (
                      this.form.orderCeilPrice.replace(/^\d+\./, "").length >
                      2
                  ) {
                      this.$toast.fail("法币最多保留小数点后2位");
                      return false;
                  }
              }
          }
          if (
              Number(this.form.orderFloorPrice) >
              Number(this.form.orderCeilPrice)
          ) {
              this.$toast.fail("最小限额不能大于最大限额");
              return false;
          }
          if (Number(this.form.orderFloorPrice) < 0) {
              this.$toast.fail("最小限额必须大于0");
              return false;
          }
            if (!this.authlist.twoPhase &&  this.authMode=="phone" && this.form.phoneCode.trim() == "") {
            this.$toast.fail("请输入验证码");
            return;
         }
         if (!this.authlist.twoPhase && this.authMode=="email" && this.form.emailCode.trim() == "") {
             this.$toast.fail("请输入验证码");
            return;
         }
          return true;
      }
  }
};
</script>

