        <van-picker  :cancel-button-text="$t('common.cancel')" :confirm-button-text="$t('common.sure')" :columns="currencyArr" @confirm="onChangeCurrency" show-toolbar @cancel="selectCurrency = false" />
      </van-popup>
      <van-cell :title='$t("common.xuanZeBiZhong")'  is-link :value="form.currency" @click="selectCurrency = true" />
      <!-- 法币 -->
      <van-popup v-model="selectFiatCurrency" position="bottom" :overlay="false">
        <van-picker  :cancel-button-text="$t('common.cancel')" :confirm-button-text="$t('common.sure')" :columns="fiatCurrencyArr" @confirm="onChangeFiatCurrency" show-toolbar @cancel="selectFiatCurrency = false" />
      </van-popup>
      <van-cell :title='$t("common.huoBi")' is-link :value="form.fiatCurrency" @click="selectFiatCurrency = true" />
    </div>

    <div class="box d2">
      <!-- 支付方式 -->
      <div class="allow-pay">
        <span class="text-pay"> {{$t("common.zhiFuFangShi")}}：</span>
        <van-checkbox-group v-model="form.allowPay">
          <van-cell-group>
            <van-cell v-for="(item, index) in payArr"  :title="item.payType | payTypeFilter" :key="index">
              <div class="canUseBox">
                  <!-- <router-link tag="span" to="/account/tradeSetting/:payType" class="gotoSet">{{ !item.canUse ? '前去预设' : ''}}</router-link> -->
                  <span class="gotoSet" @click="gotoSetting(item.payType)">{{ !item.canUse ? $t("common.qianQuYuSe") : ''}}</span>
                <van-checkbox :disabled="!item.canUse ? true : false" :name="item.payType"  class="checkbox"></van-checkbox>
              </div>
            </van-cell>
          </van-cell-group>
        </van-checkbox-group>
      </div>
    </div>

    <div class="box d2">
      <!-- 是否使用USDT支付 -->
      <div class="allow-pay">
        <van-cell-group>
          <van-cell :title="textUSDT">
            <van-checkbox v-model="form.isUSDT" />
          </van-cell>
        </van-cell-group>
      </div>
    </div>

    <div class="box d1">
      <!-- 出售价设置 -->
      <van-popup v-model="selectExchange" position="bottom" :overlay="false">
        <van-picker  :cancel-button-text="$t('common.cancel')" :confirm-button-text="$t('common.sure')" :columns="exchanges" @confirm="onChangeExchange" show-toolbar @cancel="selectExchange = false" />
      </van-popup>
      <van-cell  :title='$t("common.duiBiaoJiaoYiSuo")' is-link :value="exchangeText" @click="selectExchange = true" />
    </div>

    <div class="box">
      <!-- 溢价 -->
      <span class="text-pay"> {{$t("common.yiJia")}}{{`(${overPercentPrice})`}}</span>
      <van-cell-group class="over-percent">
        <van-field v-model="form.overPercent" @input="onChangeOverPercent" />
      </van-cell-group>
      <span class="scope">{{$t("common.yiJiaFanWei")}}：{{overPercentScope}}</span>
    </div>

    <div class="box d3">
      <!-- 最高/低 价 -->
      <van-cell-group>
        <van-field v-model="form.limitPrice" :label="textBest" />
      </van-cell-group>

      <!-- 最小限额-->
      <van-cell-group>
        <van-field v-model="form.orderFloorPrice" :label="textFloor" />
      </van-cell-group>

      <!-- 最大限额-->
      <van-cell-group>
        <van-field v-model="form.orderCeilPrice" :label="textCeil" />
      </van-cell-group>
    </div>

    <div class="box">
      <!-- 溢价 -->
      <span class="text-pay">{{$t("common.yiJiaText1")}}</span>
      <van-cell-group class="over-percent">
        <van-field v-model="form.maxOrderLimit" type="number" />
      </van-cell-group>
    </div>

    <div class="box">
      <!-- 自动回复消息 -->
      <span class="text-pay">
          {{$t("advertisement.adShouXuFei5",{msg1:textTrade})}}:
        <!-- 当{{textTrade}}家下订单之后，自动回复给{{textTrade}}家的消息: -->
      </span>
      <van-cell-group class="over-percent">
        <van-field v-model="form.autoReplyBefore" />
      </van-cell-group>
    </div>

    <div class="box">
      <!-- 必须成交次数 -->
      <span class="text-pay">{{$t("common.biXuChengJiaoCiShu")}}</span>
      <van-cell-group class="over-percent">
        <van-field v-model="form.tradeCountLimit" type="number" />
      </van-cell-group>
    </div>

    <div class="box d2">
      <!-- 卖家/买家限制 -->
      <div class="allow-pay">
        <span class="text-pay">{{$t("advertisement.maiJixXianZhi")}}：</span>
        <van-cell-group>
          <van-cell  :title='$t("common.duiFangBiXuShouJiYanZheng")'>
            <van-checkbox v-model="form.isIdentityVerify" />
          </van-cell>
          <van-cell :title='$t("common.duiFangBiXuErCiYanZheng")'>
            <van-checkbox v-model="form.isadvancedVerify" />
          </van-cell>
        </van-cell-group>
      </div>
    </div>

       <div class="box">
        <!-- 手机验证码 -->
       
        <van-cell-group class="over-percent" v-if="!authlist.twoPhase">
          
                   <!-- // 短信和邮箱验证 -->
            <van-tabs v-model="authMode" @click="selectAuthMode" class="code-box" v-if="!authlist.twoPhase">
                <!-- 短信验证 -->
                <van-tab  :title='$t("yanZheng.duanXinYanZheng")' v-if="authlist.phone" >
                    <van-field center v-model="form.phoneCode" :label='$t("yanZheng.yanZhengMa")' :placeholder='$t("account.inputVerify")' icon="clear"  @click-icon="form.phoneCode = ''" >
                        <van-button @click="getCode" :disabled="canSend" slot="button" size="small" type="primary">
                             {{$t("yanZheng.sendCode")}}
                            <span v-if="countDown > 0"> ({{countDown}})</span>
                        </van-button>
                    </van-field>
            
                </van-tab>
                <van-tab  :title='$t("common.emailYanZheng")' v-if="authlist.email">
                    <van-field center v-model="form.emailCode" :label='$t("yanZheng.yanZhengMa")'  :placeholder='$t("account.inputVerify")' icon="clear"  @click-icon="form.emailCode = ''" >
                        <van-button @click="getCode" :disabled="canSend" slot="button" size="small" type="primary">
                             {{$t("yanZheng.sendCode")}}
                            <span v-if="countDown > 0"> ({{countDown}})</span>
                        </van-button>
                    </van-field>
                </van-tab>
            </van-tabs>
        </van-cell-group>


    </div>

    <div class="submit">
      <van-button @click="_submitAdver" type="primary" :disabled="forbiddenSubmit">{{$t("advertisement.faBuJiaoYi")}}</van-button>
    </div>
  </div>
